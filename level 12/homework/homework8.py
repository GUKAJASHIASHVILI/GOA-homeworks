num = int(input("Enter a random number between 1 and 100: "))

if num >= 90 and num <= 100:
    print("Grade A")

if num >= 80 and num <= 89:
    print("Grade B")

if num >= 70 and num <= 79:
    print("Grade C")

if num >= 60 and num <= 69:
    print("Grade D")

if num >= 0 and num <= 59:
    print("Grade F")

